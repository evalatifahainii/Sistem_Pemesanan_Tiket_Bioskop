    public PembayaranBooking() {
        initComponents();
        
        DaftarBooking daftarBooking = new DaftarBooking();
        
        namaPembeli = daftarBooking.nama;
        
        try {
            String query = "select * FROM booking WHERE nama='"+ namaPembeli +"'";
            c = Koneksi.configDB();
            s = c.createStatement();
            rs = s.executeQuery(query);
            while(rs.next()){
                nama = rs.getString("nama");
                namaFilm = rs.getString("namaFilm");
                studio = rs.getString("studio");
                jam = rs.getString("jam");
                tanggal = rs.getString("forTanggal");
                kursi = rs.getString("kursi");
                jumlah = (String) rs.getString("jumlah");
                totalHarga = rs.getString("totalHarga");
                
                namaLabel.setText(nama);
                namaFilmLabel.setText(namaFilm);
                studioLabel.setText(studio);
                jamLabel.setText(jam);
                tanggalLabel.setText(tanggal);
                kursiLabel.setText(kursi);
                jumlahLabel.setText(jumlah);
                totalHargaLabel.setText(totalHarga);
            }
            
        } catch (SQLException e) {
            System.out.println(e);
        }
    }

